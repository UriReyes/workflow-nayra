    public function getForms()
    {
        $forms = Screen::select('id', 'title')->where('type', 'FORM')->orderByDesc('id')->get();
        $formsList = [];
        foreach ($forms as $form) {
            array_push($formsList, ['value' => $form->id, 'content' => $form->title]);
        }

        return response()->json(['forms' => $formsList]);
    }
